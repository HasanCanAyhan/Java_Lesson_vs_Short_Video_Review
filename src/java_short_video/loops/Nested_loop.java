package java_short_video.loops;

public class Nested_loop {

    public static void main(String[] args) {


        // print 1 ~ 5 for 5 times

        for (int j = 1; j <= 5 ; j++) { // from up to bottom


            for (int i = 1; i <= 5; i++){ // i: 1 ~ 5 // from left to right

                System.out.print(i+" ");

            }


            System.out.println();


        }








    }

}
